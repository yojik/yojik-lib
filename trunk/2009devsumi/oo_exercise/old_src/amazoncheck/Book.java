package amazoncheck;

public class Book {
	String title;
	int price;
	int usedPrice;
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getUsedPrice() {
		return usedPrice;
	}
	public void setUsedPrice(int usedPrice) {
		this.usedPrice = usedPrice;
	}
}
